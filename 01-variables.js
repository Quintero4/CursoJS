//var 
//var es una variable que se puede reasignar y redeclarar
var helloWorld = "Hello, World!"
console.log(helloWorld) 

var helloWorld = "Hello again, World!"
console.log(helloWorld) 

//let
//let es una variable que se puede reasignar pero no redeclarar
let  helloWorld2 = "hola javascript 2"
console.log(helloWorld2) 

helloWorld2 = "hola de nuevo javascript 2"
console.log(helloWorld2) 

//const
const helloWorld3 = "hola javascript 3"
console.log(helloWorld3)    

helloWorld3 = "hola de nuevo javascript 3"
console.log(helloWorld3)   //Da error porque las constantes no se pueden reasignar
